package java_loop;
// 자바의 반복문 종류 및 각각의 기본적인 사용법을 코드로 구현
// 이 문제는 자바의 여러 반복문 종류와 각각의 개념 및 기본 사용법에 대한 문제.

public class Java100_for_Iteration001 {
	public static void main(String[] args) {
		// [!] : 자바의 반복문 종류 --> 크게 봤을 때 4가지
		// for
		// while
		// do~ while
		// 향상된 for문

		// [1] : for
		for(int i=1;i<11;i++) { //조건식 --> 참인 동안 실행
			System.out.print(i+" ");	
		}
		//System.out.println("");
	}
	
}




